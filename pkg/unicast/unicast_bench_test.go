package unicast

import (
	"testing"
)

// Single labeled unicast NLRI
var luNLRISingle = []byte{0x38, 0x00, 0x00, 0x31, 0x0a, 0x00, 0x00, 0x00}

// Multiple labeled unicast NLRIs with pathID
var luNLRIMulti = []byte{
	0x00, 0x00, 0x00, 0x01, 0x30, 0x00, 0x00, 0x31, 0xc0, 0xa8, 0x50,
	0x00, 0x00, 0x00, 0x01, 0x38, 0x00, 0x00, 0x31, 0x5a, 0x1e, 0x0a, 0x01,
	0x00, 0x00, 0x00, 0x01, 0x30, 0x00, 0x00, 0x31, 0x09, 0x00, 0xcb,
	0x00, 0x00, 0x00, 0x01, 0x30, 0x00, 0x00, 0x31, 0x09, 0x00, 0x67,
	0x00, 0x00, 0x00, 0x01, 0x30, 0x00, 0x00, 0x31, 0x09, 0x00, 0x22,
}

func BenchmarkUnmarshalLUNLRI_Single(b *testing.B) {
	b.ReportAllocs()
	for i := 0; i < b.N; i++ {
		_, err := UnmarshalLUNLRI(luNLRISingle, false)
		if err != nil {
			b.Fatal(err)
		}
	}
}

func BenchmarkUnmarshalLUNLRI_Multi(b *testing.B) {
	b.ReportAllocs()
	for i := 0; i < b.N; i++ {
		_, err := UnmarshalLUNLRI(luNLRIMulti, true)
		if err != nil {
			b.Fatal(err)
		}
	}
}
