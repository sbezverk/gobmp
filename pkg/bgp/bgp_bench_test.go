// BGP Update with MP_REACH_NLRI (IPv6 unicast)
var bgpUpdateIPv6 = []byte{
	0x00, 0x00, 0x00, 0x2C, 0x40, 0x01, 0x01, 0x02,
	0x40, 0x02, 0x0A, 0x02, 0x02, 0x00, 0x00, 0xFD,
	0xE9, 0x00, 0x00, 0xFD, 0xEB, 0x80, 0x0E, 0x18,
	0x00, 0x02, 0x01, 0x10, 0x00, 0x00, 0x00, 0x00,
	0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xFF, 0xFF,
	0x0A, 0x98, 0xB7, 0x0B, 0x00, 0x10, 0x20, 0x01,
}

func BenchmarkUnmarshalBGPUpdate(b *testing.B) {
	b.ReportAllocs()
	for i := 0; i < b.N; i++ {
		_, err := UnmarshalBGPUpdate(bgpUpdateIPv6)
		if err != nil {
			b.Fatal(err)
		}
	}
}

